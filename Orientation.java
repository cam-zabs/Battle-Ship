package cs3500.pa04.model;

/**
 * The Orientation enum represents the orientation of a ship on a game board.
 * It can be either vertical or horizontal.
 */
public enum Orientation {

  /**
   * The vertical orientation.
   * Indicates that a ship is positioned vertically on a game board.
   */
  VERTICAL,

  /**
   * The horizontal orientation.
   * Indicates that a ship is positioned horizontally on a game board.
   */
  HORIZONTAL;
}
